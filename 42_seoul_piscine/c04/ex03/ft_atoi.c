int	ft_atoi(char *str)
{
	int	num;
	int	minus;

	num = 0;
	minus = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			minus *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + minus * (*str - '0');
		str++;
	}
	return (num);
}
