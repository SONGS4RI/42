#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	while ((i * i) <= nb)
	{
		if(nb % i != 0)
			return (1);
		i++;
	}
	if (nb == 2 || nb == 3)
		return (1);
	return (0);
}
int main()
{
	int num = 2;
	printf("%d",ft_is_prime(num));
}

