#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (*str)
	{
		if (*str >= 65 && *str <= 90)
			*str += 32;
		str++;
		i++;
	}
	return (str - i);
}
